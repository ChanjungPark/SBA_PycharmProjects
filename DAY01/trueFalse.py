# 참과 거짓
# 숫자 : 0은 거짓, 0이 아니면 참
# 문자열 : 비어있으면 거짓, 1글자 이상이면 참
# 리스트나 튜플은 0개이면 거짓

# if 조건식
su = 0
if su :
    print('참')
else :
    print('거짓')

# 리스트
myList = [1, 2, 3]
if myList :
    print('리스트 참')
else :
    print('리스트 거짓')

# 튜플
mytuple = ()
if mytuple :
    print('튜플 참')
else :
    print('튜플 거짓')